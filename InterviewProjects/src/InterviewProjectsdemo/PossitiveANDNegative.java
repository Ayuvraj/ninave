package InterviewProjectsdemo;

public class PossitiveANDNegative {

	public static void main(String[] args) {
		
		double number = -12.3;
		
		System.out.println("POSSITVE AND NEGATIVE NUMBERS FIND BY USING IF ELSE CONDTIONS");
		
		if (number < 0.0)
		{
			System.out.println(number + " is a NEGATIVE NUMBER");
		}
		else if (number > 0.0)
		{
			System.out.println(number + " is a POSSITIVE NUMBER");
		}
		else
			System.out.println(number + " is 0");

	}

}
